# f = 40.0 kHz
idx_40 = f_data.index(40.0)
dc2_40 = (V_data[idx_40 + 1] - V_data[idx_40 - 1]) / (2 * h)
dc4_40 = (-V_data[idx_40 + 2] + 8 * V_data[idx_40 + 1] - 8 * V_data[idx_40 - 1] + V_data[idx_40 - 2]) / (12 * h)

